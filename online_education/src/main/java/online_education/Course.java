package online_education;

public class Course {
	private String pictitles ;
	private int lesson ;
	private Teacher teacher ;
	public String getPictitles() {
		return pictitles;
	}
	public void setPictitles(String pictitles) {
		this.pictitles = pictitles;
	}
	public int getLesson() {
		return lesson;
	}
	public void setLesson(int lesson) {
		this.lesson = lesson;
	}
	public Teacher getTeacher() {
		return teacher;
	}
	public void setTeacher(Teacher teacher) {
		this.teacher = teacher;
	}
	
	
}
